#' @import dplyr
#' 
################################################################################
## 01. 시군구 코드 오류건 수정 in 수치지도 데이터
################################################################################
##==============================================================================
## 01.01. 시군구 레벨 수치지도
##==============================================================================
cty <- cty %>% 
  mutate(cty_cd = ifelse(cty_cd %in% "43780", "43745", cty_cd)) 


##==============================================================================
## 01.02. 시군구 레벨 수치지도 저장
##==============================================================================
save(cty, file = here::here("data", "cty.rda"))


################################################################################
## 02. 시군구 코드 오류건 수정 in 위치 데이터
################################################################################
##==============================================================================
## 02.01. 병원 위치정보 데이터
##==============================================================================
hospital_info <- hospital_info %>% 
  mutate(cty_cd = ifelse(cty_cd %in% "43780", "43745", cty_cd)) 


##==============================================================================
## 02.02. 약국 위치정보 데이터
##==============================================================================
pharmacy_info <- pharmacy_info %>% 
  mutate(cty_cd = ifelse(cty_cd %in% "43780", "43745", cty_cd)) 
  
##==============================================================================
## 02.03. 위치정보 데이터 저장
##==============================================================================
save(hospital_info, file = here::here("data", "hospital_info.rda"))
save(pharmacy_info, file = here::here("data", "pharmacy_info.rda"))
